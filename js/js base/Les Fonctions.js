function diviserParDeux (nb){
    return nb/2
}

function somme (a,b,c){
    return a + b + c
}

function factorielle (nb) {
    let fact = 1
    for (let i = 1; i <= nb; i++) {
        fact * 1
    }
    return fact
}

function majorite (age) {
    if (age < 18){
        return false
    } else {
        return true
    }
}